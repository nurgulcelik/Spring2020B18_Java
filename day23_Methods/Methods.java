package day23_Methods;
public class Methods {//methods can be declared in this class
    public static void main(String[] args) {
        printHello5Times();

    }//after another method can be declare

    public static void printHello5Times() {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello  World");
        }
    }
}