package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\tHello");
        // t means a paragraph space

        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("My\nName\nIs\nNurgul");
        // it means new line, starts with new line

        // print: My favorite Tv show is "Criminal Minds"
        System.out.println("My Favorite TV show is\"CRITICAL MINDS\"");

// \means double quote, prints double quote to the console

}



}
